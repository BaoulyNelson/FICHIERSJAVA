public class Type{
    public static void main(String[] args) {
        System.out.println("Type float :");
        System.out.println("Nombre d'octets : " + Float.BYTES);
        System.out.println("Valeur maximale : " + Float.MAX_VALUE);
        System.out.println("Valeur minimale : " + Float.MIN_VALUE);

        System.out.println("Type double :");
        System.out.println("Nombre d'octets : " + Double.BYTES);
        System.out.println("Valeur maximale : " + Double.MAX_VALUE);
        System.out.println("Valeur minimale : " + Double.MIN_VALUE);

        System.out.println("Type short :");
        System.out.println("Nombre d'octets : " + Short.BYTES);
        System.out.println("Valeur maximale : " + Short.MAX_VALUE);
        System.out.println("Valeur minimale : " + Short.MIN_VALUE);

        System.out.println("Type long :");
        System.out.println("Nombre d'octets : " + Long.BYTES);
        System.out.println("Valeur maximale : " + Long.MAX_VALUE);
        System.out.println("Valeur minimale : " + Long.MIN_VALUE);

        System.out.println("Type byte :");
        System.out.println("Nombre d'octets : " + Byte.BYTES);
        System.out.println("Valeur maximale : " + Byte.MAX_VALUE);
        System.out.println("Valeur minimale : " + Byte.MIN_VALUE);
    }
}
